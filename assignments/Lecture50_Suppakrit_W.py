def plusNumber(x, y):
    print(x + y)
def minusNumber(x, y):
    print(x - y)
def multiplyNumber(x, y):
    print(x * y)
def divideNumber(x, y):
    print(x / y)

x = int(input("Input your first number: "))
y = int(input("Input your second number: "))
plusNumber(x, y)
minusNumber(x, y)
multiplyNumber(x, y)
divideNumber(x, y)